package raisetech.student.management.exception;

public class TestRegisterException extends Exception {

  public TestRegisterException() {
    super();
  }

  public TestRegisterException(String message) {
    super(message);
  }

  public TestRegisterException(String message, Throwable cause) {
    super(message, cause);
  }

  public TestRegisterException(Throwable cause) {
    super(cause);
  }
}