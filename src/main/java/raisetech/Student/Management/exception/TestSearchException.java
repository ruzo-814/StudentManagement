package raisetech.student.management.exception;

public class TestSearchException extends Exception {

  public TestSearchException() {
    super();
  }

  public TestSearchException(String message) {
    super(message);
  }

  public TestSearchException(String message, Throwable cause) {
    super(message, cause);
  }

  public TestSearchException(Throwable cause) {
    super(cause);
  }

}
